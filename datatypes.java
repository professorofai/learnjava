public class datatypes {
     public static void main(String[] args) {
        
    byte myByte = 18;       // 1 byte
    System.out.println(myByte);

    short myShort = 32767;   // 2 bytes
    System.out.println(myShort);

    int myInt = -21;     // 4 bytes
    System.out.println(myInt);

    long myLong = 123456789L;   // 8 bytes
    System.out.println(myLong);

    float myFloat = 5.99f;   // 4 bytes
    System.out.println(myFloat);

    double myDouble = 10.10;   // 8 bytes
    System.out.println(myDouble);

    var myChar = 'A';     // 2 bytes
    System.out.println(myChar);
    var myBool = true;   // 1 bit
    System.out.println(myBool);
    var myString = "Hello, World!"; // String (not a primitive data type)
    System.out.println(myString);
     }
}
